function cpsInfo
    %cpsInfo Display information on Capsaicin Plot Tools
    %   Capsaicin (CPS) Plot Tools simplify common plotting tasks, such as
    %   adding reference lines or scaling all axes to a global minumum and
    %   maximum. Some functions aim to provide an improvement of built-in
    %   MatLab function of the corresponding name. For example, compare
    %   cpsFindFig with figure and cpsText with text.
    %
    %   Because all Capsaicin functions start with 'cps', typing 'cps' in
    %   command window or editor followed by the tab-key lists the
    %   functions. Provided, of course, that tab completion is enabled in
    %   Matlab's preferences.
    %
    %   Function       Description
    %   <a href="matlab:help cpsFindFig">cpsFindFig</a>     Create or find a figure-window by name.
    %   <a href="matlab:help cpsInfo">cpsInfo</a>        Display this help page.
    %   <a href="matlab:help cpsLabelPanels">cpsLabelPanels</a>  XXX
    %   <a href="matlab:help cpsRefline">cpsRefline</a>     XXX
    %   <a href="matlab:help cpsText">cpsText</a>        XXX
    %   <a href="matlab:help cpsTileFigs">cpsTileFigs</a>    XXX
    %   <a href="matlab:help cpsUnifyAxes">cpsUnifyAxes</a>   Unify axes of multiple subplots
    %
    %   Copyright 2016 Jacob Duijnhouwer
    
    help cpsInfo;
end